'''Função para conversão de gramas (g) para quilogramas (kg).
Função para conversão de quilogramas (kg) para gramas (g).'''

def gramas_para_quilogramas(gramas):
    """
    Converte gramas para quilogramas.
    """
    
    return gramas / 1000

def quilogramas_para_gramas(quilogramas):
   """ Converte quilogramas para gramas.
   """
   return quilogramas * 1000
